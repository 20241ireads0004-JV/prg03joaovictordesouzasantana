    private int numero = 0;
    private int fatorial = 1;
    private String formula = "";
    
    public void setValor(int num){
        numero = num;
        int fat = 1;
        String fmla = "";
        
        for(int c = num; c > 1; c--){
            fat *= c;
            
            fmla+= c + " x ";
        }
        
        fmla += "1 ="; //pra concatenar com o 1
        
        fatorial = fat;
        formula = fmla;
            
    }
    
    public int getFatorial(){
        return fatorial;
    }
    
    public String getFormula(){
        return formula;
    }
